SELECT Н_ЛЮДИ.ОТЧЕСТВО, Н_ВЕДОМОСТИ.ДАТА, Н_СЕССИЯ.ИД 
    FROM Н_ЛЮДИ 
    RIGHT JOIN Н_ВЕДОМОСТИ ON Н_ЛЮДИ.ИД = Н_ВЕДОМОСТИ.ЧЛВК_ИД 
    RIGHT JOIN Н_СЕССИЯ ON Н_ЛЮДИ.ИД = Н_СЕССИЯ.ЧЛВК_ИД 
    WHERE Н_ЛЮДИ.ИД > 100865 
        AND Н_ВЕДОМОСТИ.ЧЛВК_ИД < 163249 
        AND Н_СЕССИЯ.УЧГОД < '2011/2012';

CREATE INDEX ON Н_ЛЮДИ (ИД);
CREATE INDEX ON Н_ВЕДОМОСТИ (ЧЛВК_ИД);
CREATE INDEX ON Н_СЕССИЯ (УЧГОД, ЧЛВК_ИД);